#include <stdio.h>
/**
 * mod - mod of two intigers
 * @a: for first int
 * @b: for second int
 * Return: the final answer
 */
int mod(int a, int b)
{
	return (a % b);
}
/**
 * add - add two intigers
 * @a: for first int
 * @b: for second int
 * Return: addition
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * div - divide two intigers
 * @a: for first int
 * @b: for second int
 * Return: division result
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * sub - subtract two intigers
 * @a: for first int
 * @b: for second int
 * Return: subtracted final result
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplication of two intigers
 * @a: for first int
 * @b: for second int
 * Return: multiplication result
 */
int mul(int a, int b)
{
	return (a * b);
}
